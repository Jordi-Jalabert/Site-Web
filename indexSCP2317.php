<h1>SCP-2317</h1>
<p>Objet # : SCP-2317
<br /> Classe : [DONNÉES SUPPRIMÉES]
<br /> Désignation d'Accréditation : CODE CAUCHEMAR ROUGE VIF
<br />Description : SCP-2317 est une entité primordiale connue comme "Le <font color="#9b9696"> ███ ████████ </font> , Dévoreur de Mondes" (nom censuré intentionnellement). Le Codex Erikesh indique qu'il a été capturé et emprisonné par des mystiques Erikeshiens vers 1894 AEC.
 Un extrait du passage relatif à l'objet se trouve ci-après :
</p>
<p>"Tant que tant de gens croiront que nous faisons des choses terribles, le monstre continuera à croire que nous faisons des choses terribles. Les symboles ont un pouvoir, docteur.
 Les anciens dieux le savent et les anciens dieux sont liés par lui.
 Les Scarlet King n'a pas les yeux pour voir la brutalité, n'a pas les oreilles pour entendre les cris, n'a pas le nez pour sentir le sang. Mais le roi Scarlet peut sentir la peur et nous lui avons donné la peur. La peur seule est tout ce dont elle avait besoin " „
~ Oliver Targus à propos du roi écarlate.
<br />
““ Je sais que la Fondation n’approuve pas l’utilisation du terme, mais c’est toujours le cas.
 Le cerf est un dieu, et ce n’est pas un petit dieu local. 
Ce n’est pas un des dieux doux de la Terre, ni esprit plus faible qui est lié par la force de la conviction de l'homme ". „
~ Consultant <font color="#9b9696"> ████████ ████████ </font> décrivant SCP-2845.
<br />
 </p>
 <p>
Les Anciens Dieux, parfois aussi appelés Dieux du Bas Elder, sont un panthéon lâche de divinités eldritch, dont beaucoup servent d'antagonistes majeurs dans l'univers de la Fondation SCP.
<br /> Lors de la découverte initiale, quatre des sept chaînes étaient cassées, et trois étaient intactes : la rupture de la cinquième chaîne en <font color="#9b9696"> ████ </font> correspondit directement à un Événement <font color="#9b9696"> ███████████ </font> dans notre monde. La sixième chaîne, qui rompit en <font color="#9b9696"> ████ </font>, coïncida également avec un Événement <font color="#9b9696"> ███████████ </font> bien plus violent, résultant finalement en la mort de deux millions de personnes.
</p> <p>
L'analyse des textes supplémentaires retrouvés au moment du confinement a permis de définir la Procédure 220-Calabasas en tant que moyen efficace de maintenir le confinement jusqu'à ce qu'une méthode efficace de réparation ou de rattachement desdites chaînes soit découverte.
 Les procédures de confinement ont été modifiées afin d'inclure la Procédure 220-Calabasas.
 Les enregistrements montrant que la séquence d'Événements <font color="#9b9696"> ███████████ </font> présente une augmentation exponentielle de la létalité, il peut être extrapolé que la défaillance de la chaîne finale (SCP-2317-G), résulterait en un Scénario de Fin du Monde de Classe XK.
<br />
</p> <p>
Pour cette raison, tous les efforts possibles doivent être mis en œuvre afin de s'assurer de la réussite de la Procédure 220-Calabasas.
</p>
